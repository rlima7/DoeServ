package com.example.doacao_.projeto_integrador.modelo;

public class Usuario {
    private String u_nome, u_email, u_cpf, u_login, u_senha, u_senha2;

    public Usuario() {

    }

    public String getU_nome() {
        return u_nome;
    }

    public void setU_nome(String u_nome) {
        this.u_nome = u_nome;
    }

    public String getU_email() {
        return u_email;
    }

    public void setU_email(String u_email) {
        this.u_email = u_email;
    }

    public String getU_cpf() {
        return u_cpf;
    }

    public void setU_cpf(String u_cpf) {
        this.u_cpf = u_cpf;
    }

    public String getU_login() {
        return u_login;
    }

    public void setU_login(String u_login) {
        this.u_login = u_login;
    }

    public String getU_senha() {
        return u_senha;
    }

    public void setU_senha(String u_senha) {
        this.u_senha = u_senha;
    }

    public String getU_senha2() {
        return u_senha2;
    }

    public void setU_senha2(String u_senha2) {
        this.u_senha2 = u_senha2;
    }
}
