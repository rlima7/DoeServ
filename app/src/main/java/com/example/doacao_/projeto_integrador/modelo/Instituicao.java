package com.example.doacao_.projeto_integrador.modelo;

public class Instituicao {
    private String i_nome, i_cnpj, i_telefone, i_email, i_desc, i_nomeUsuario,
    i_senha, i_senha2;

    public Instituicao() {

    }

    public String getI_nome() {
        return i_nome;
    }

    public void setI_nome(String i_nome) {
        this.i_nome = i_nome;
    }

    public String getI_cnpj() {
        return i_cnpj;
    }

    public void setI_cnpj(String i_cnpj) {
        this.i_cnpj = i_cnpj;
    }

    public String getI_telefone() {
        return i_telefone;
    }

    public void setI_telefone(String i_telefone) {
        this.i_telefone = i_telefone;
    }

    public String getI_email() {
        return i_email;
    }

    public void setI_email(String i_email) {
        this.i_email = i_email;
    }

    public String getI_desc() {
        return i_desc;
    }

    public void setI_desc(String i_desc) {
        this.i_desc = i_desc;
    }

    public String getI_nomeUsuario() {
        return i_nomeUsuario;
    }

    public void setI_nomeUsuario(String i_nomeUsuario) {
        this.i_nomeUsuario = i_nomeUsuario;
    }

    public String getI_senha() {
        return i_senha;
    }

    public void setI_senha(String i_senha) {
        this.i_senha = i_senha;
    }

    public String getI_senha2() {
        return i_senha2;
    }

    public void setI_senha2(String i_senha2) {
        this.i_senha2 = i_senha2;
    }
}
